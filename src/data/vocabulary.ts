// Get words by level
export const getWordsByLevel = (level: number): Word[] => {
  return vocabulary.filter(word => word.level === level);
};

// Get words by category
export const getWordsByCategory = (category: string): Word[] => {
  return vocabulary.filter(word => word.category === category);
};

// Get random words for quiz
export const getRandomWords = (count: number, level?: number): Word[] => {
  const filtered = level ? vocabulary.filter(w => w.level <= level) : vocabulary;
  const shuffled = [...filtered].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
};
